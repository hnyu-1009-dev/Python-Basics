# sys       | Python解释器参数与环境                  | argv, path, exit(), version
# math      | 数学计算（更高精度）                     | sqrt(), pow(), sin(), log(), ceil(), floor()
# time      | 时间处理与格式化                        | time(), sleep(), localtime(), strftime()
# random    | 随机数生成与概率抽样                     | randint(), choice(), shuffle(), sample()
